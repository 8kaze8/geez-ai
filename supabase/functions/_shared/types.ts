/**
 * Aggregated user context assembled by the Memory Agent.
 *
 * Passed to the Research, Route, and Content agents to personalise output.
 */
export interface UserContext {
  /** Supabase Auth user UUID. */
  userId: string;
  /** Snapshot of user_profiles row. */
  profile: UserProfileSnapshot | null;
  /** Snapshot of travel_personas row. */
  persona: TravelPersonaSnapshot | null;
  /** Recently visited places (up to 100). */
  visitedPlaces: VisitedPlaceSnapshot[];
  /** Most recent trip feedback entries (up to 10). */
  recentFeedback: TripFeedbackSnapshot[];
  /** Inferred pace from feedback history. */
  pacePreference: PacePreference;
  /** Strongly preferred activity types derived from feedback. */
  strongPreferences: string[];
}

/** Subset of user_profiles used in context. */
export interface UserProfileSnapshot {
  ageGroup?: string;
  travelCompanion?: string;
  defaultBudget?: BudgetLevel;
  preferredActivities: string[];
  foodPreferences: Record<string, boolean>;
  pacePreference: PacePreference;
  morningPerson: boolean;
  crowdTolerance: "low" | "medium" | "high";
}

/** Subset of travel_personas used in context. */
export interface TravelPersonaSnapshot {
  foodieLevel: number;
  historyBuffLevel: number;
  natureLoverLevel: number;
  adventureSeekerLevel: number;
  cultureExplorerLevel: number;
  discoveryScore: number;
  explorerTier: string;
}

/** Subset of visited_places used in context. */
export interface VisitedPlaceSnapshot {
  placeId?: string;
  placeName: string;
  city: string;
  country: string;
  category?: string;
  userRating?: number;
  visitedAt?: string;
}

/** Subset of trip_feedback used in context. */
export interface TripFeedbackSnapshot {
  routeId: string;
  overallRating?: number;
  paceFeedback?: "too_intense" | "just_right" | "too_slow";
  createdAt: string;
}

